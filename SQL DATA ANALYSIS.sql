CREATE DATABASE PROJECT

USE PROJECT

CREATE TABLE INDIAN_BUS_FARE(AGENCY varchar(20),
PICKUP varchar(10),
DESTINATION varchar(15),
BUSTYPE varchar(15), 
TRAVELDATE date, 
FAREPRICE decimal(8,2),
TOTALSEATS int,
DURATION int)

# IMPORT DATA FROM INDIAN_BUS_FARE CSV FILE INTO INDIAN_BUS_FARE TABLE

# DATA VALIDATION

SELECT *
FROM INDIAN_BUS_FARE
WHERE AGENCY IS NULL OR
      PICKUP IS NULL OR
      DESTINATION IS NULL OR
      BUSTYPE IS NULL OR
      TRAVELDATE IS NULL OR
      FAREPRICE IS NULL OR
      TOTALSEATS IS NULL OR
      DURATION IS NULL;

#Q1. Find the top 5 most profitable routes based on total fare revenue generated.

SELECT PICKUP, DESTINATION, SUM(FAREPRICE) AS TOTAL_FARE
FROM INDIAN_BUS_FARE
GROUP BY PICKUP, DESTINATION
ORDER BY TOTAL_FARE DESC
LIMIT 5;

#Q2 Identify the agency that has the highest average fare price for Volvo buses.

SELECT AGENCY, AVG(FAREPRICE) AS AVG_FARE
FROM INDIAN_BUS_FARE
WHERE BUSTYPE = 'VOLVO'
GROUP BY AGENCY
ORDER BY AVG_FARE DESC
LIMIT 1;

#Q3 Find the top 3 destinations with the highest number of bookings for each bus type.

CREATE VIEW HIGH_BOOK AS
(SELECT DESTINATION, BUSTYPE, COUNT(*) AS NO_OF_BOOK
FROM INDIAN_BUS_FARE
GROUP BY DESTINATION, BUSTYPE)

CREATE VIEW TOP_3 AS (SELECT *, DENSE_RANK() OVER ( PARTITION BY BUSTYPE ORDER BY NO_OF_BOOK DESC) AS RANKING
FROM HIGH_BOOK)

SELECT *
FROM TOP_3
WHERE RANKING <= 3;

#Q4 Calculate the monthly revenue generated by each agency and rank them month-wise.

CREATE VIEW MONTHS AS (SELECT AGENCY, DATE_FORMAT(TRAVELDATE,'%M') AS MONTH_NAME, SUM(FAREPRICE) AS TOTAL_FARE
FROM INDIAN_BUS_FARE
GROUP BY AGENCY, MONTH_NAME
)

SELECT *, DENSE_RANK() OVER (PARTITION BY MONTH_NAME ORDER BY TOTAL_FARE DESC)
FROM MONTHS;

#Q5 Find the route with the maximum average travel duration for every bus type.

WITH MAX_AVG_TRAVEL AS (SELECT PICKUP, DESTINATION, BUSTYPE, AVG(DURATION) AS TOTAL_AVG
FROM INDIAN_BUS_FARE
GROUP BY PICKUP, DESTINATION, BUSTYPE),

AVG_DUR_EACH_BUS AS (SELECT *, DENSE_RANK() OVER (PARTITION BY BUSTYPE ORDER BY TOTAL_AVG DESC) AS RANKING
FROM MAX_AVG_TRAVEL)

SELECT PICKUP, DESTINATION, BUSTYPE, TOTAL_AVG as MAX_AVG
FROM AVG_DUR_EACH_BUS
WHERE RANKING = 1;

#Q6 Find the percentage contribution of each bus type to the total revenue generated.

CREATE VIEW BUSTYPE_REVENUE AS (SELECT BUSTYPE , SUM(FAREPRICE) AS TOTAL_PRICE
FROM INDIAN_BUS_FARE
GROUP BY BUSTYPE)

SELECT BUSTYPE, (TOTAL_PRICE/SUM(TOTAL_PRICE) OVER ()) * 100 AS PERCENTAGE_CONTRIBUTION
FROM BUSTYPE_REVENUE;

#Q7 Determine the busiest travel month based on total seats booked across all agencies.

SELECT DATE_FORMAT(TRAVELDATE,'%M') AS MONTH_NAME, SUM(TOTALSEATS) AS TOTAL_SEAT
FROM INDIAN_BUS_FARE
GROUP BY MONTH_NAME
ORDER BY TOTAL_SEAT DESC
LIMIT 1;

#Q8 Identify consecutive travel dates where the same agency operated buses between the same source and destination.

WITH CONSECUTIVE_TRAVEL AS (SELECT AGENCY, PICKUP, DESTINATION, TRAVELDATE, LEAD(TRAVELDATE) OVER (PARTITION BY AGENCY, PICKUP, DESTINATION ORDER BY TRAVELDATE) AS NEXTDATE
FROM INDIAN_BUS_FARE)

SELECT AGENCY, PICKUP, DESTINATION
FROM CONSECUTIVE_TRAVEL
WHERE DATEDIFF(TRAVELDATE,NEXTDATE) = -1;

#Q9 Find all routes where the travel duration is greater than the average duration of buses starting from the same pickup.

SELECT PICKUP, DESTINATION 
FROM INDIAN_BUS_FARE A
WHERE DURATION > (SELECT AVG(DURATION) 
				  FROM INDIAN_BUS_FARE B
                  WHERE A.PICKUP = B.PICKUP);

#Q10 Calculate the running total of seats booked for each agency ordered by travel date.

SELECT AGENCY, TRAVELDATE, TOTALSEATS, SUM(TOTALSEATS) OVER (PARTITION BY AGENCY ORDER BY TRAVELDATE) AS RUNNING_TOTAL_SEAT
FROM INDIAN_BUS_FARE;

